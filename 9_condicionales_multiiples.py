num_uno = int(input("Cual es el numero que deseas convertir?: "))

if num_uno == 1:
    print("El numero es 'Uno'")
elif num_uno == 2:
    print("El numero es: 'Dos")
elif num_uno == 3:
    print("El numero es: 'Tres'")
elif num_uno == 4:
    print("El numero es: 'Cuatro'")
elif num_uno == 5:
    print("El numero es: 'Cinco'")
elif num_uno == 6:
    print("El numero es: 'Seis'")
else:
    print("Este programa solo puede convertir hasta el numero 6.\n \nBAY!")
