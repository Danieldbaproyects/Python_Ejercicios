# TIPO DE DATO ENTERO O LARGO
numero = 15
print(f"la variable {numero} es de tipo",
 type(numero))

# TIPO DE DATO FLOTANTE
num_flotante = 15.6
print("\n",num_flotante, type(num_flotante))

# TIPO DE DATO COMPLEJO
num_complejo = 5 + 6j
print("\n",num_complejo, type(num_complejo))

# TIPO DE DATO STRING
nombre = 'Daniel'
print("\n",nombre, type(nombre))

# TIPO DE DATO BOOLEANO
verdadero_falso = 3 == 4
print("\n",verdadero_falso, type(verdadero_falso))
